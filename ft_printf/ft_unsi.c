#include "ft_printf.h"

void	ft_unsi(unsigned long n, int *p)
{
	if (n < 10)
	{
		ft_putchar((n + 48), p);
	}
	else
	{
		ft_putnbr(n / 10, p);
		ft_putnbr(n % 10, p);
	}
}
